/**
 * Regenerates netlify/functions/_shared/social-cards/font-data.generated.ts from Inter
 * static TTFs, subset to printable ASCII + Latin-1 (all a card ever renders).
 * See scripts/social-cards/README.md for usage.
 */
import { readFile, writeFile } from 'node:fs/promises';
import { resolve } from 'node:path';
import subsetFont from 'subset-font';

const fontDir = process.argv[2];
if (!fontDir) {
  console.error(
    'Usage: node generate-font-data.mjs <dir containing Inter-{Regular,SemiBold,Bold}.ttf>'
  );
  process.exit(1);
}

let chars = '';
for (let c = 0x20; c <= 0x7e; c++) chars += String.fromCharCode(c);
for (let c = 0xa0; c <= 0xff; c++) chars += String.fromCharCode(c);

const weights = [
  ['Inter-Regular', 400],
  ['Inter-SemiBold', 600],
  ['Inter-Bold', 700],
];

const parts = [];
for (const [name, weight] of weights) {
  const buf = await readFile(resolve(fontDir, `${name}.ttf`));
  const sub = await subsetFont(buf, chars, { targetFormat: 'truetype' });
  console.log('%s: %d -> %d bytes', name, buf.length, sub.length);
  parts.push({ weight, b64: sub.toString('base64') });
}

const out = `// Generated by scripts/social-cards/generate-font-data.mjs — do not edit.
// Inter (https://rsms.me/inter/, SIL OFL 1.1) subset to printable ASCII + Latin-1,
// vendored so the renderer never fetches fonts at runtime (Lambda has no system fonts).
export interface FontData {
  weight: number;
  base64: string;
}

export const INTER_FONTS: FontData[] = [
${parts.map((p) => `  { weight: ${p.weight}, base64: '${p.b64}' },`).join('\n')}
];
`;

const target = resolve(
  import.meta.dirname,
  '../../netlify/functions/_shared/social-cards/font-data.generated.ts'
);
await writeFile(target, out);
console.log('wrote %s (%d bytes)', target, out.length);
